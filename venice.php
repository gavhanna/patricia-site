        <h2>Personal Structures Venice</h2>
        <p>I am working on an installation ‘<strong>and the world goes on</strong>’ which is part of ‘Personal Structures’ an exhibition organised by the Dutch Global Arts Affairs Foundation and hosted by the European Cultural Centre in Palazzo Mora and Palazzo Bembo in Venice. This exhibition is organized within the context of the Venice Biennale and runs from 13 th May until 26 th November 2017.</p>
        <p>I have a small room in Palazzo Mora with dark, powerful roof rafters that are hundreds of years old. The work, which consists of 2 videos, tree sculptural forms, shadow and neon, will interact with the space and rafters. These elements can only come together when the work is being installed in April.</p>
        <p>I will post images of the finished work before the official opening on May 11<sup>th</sup>/12<sup>th</sup>.</p>
        <p>There will be a catalogue of the exhibition issued by the GAA Foundation.</p>
        <p>Personal Structures has been running since 2002 in the Venice Biennale. More information on the past projects including a virtual tour of the spaces can be seen on their website.</p>
