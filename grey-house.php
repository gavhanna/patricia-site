        <h2>THE GREY HOUSE</h2>
        <p>The Grey House was a site specific installation in a derelict house near Belturbet, Co Cavan in 1993-94. It was part of a 10 year series called "Marking the Land" which looked at cultural identity and the effects of migration and displacement.<p>
        <p>The derelict house was rented from a farmer with the assistance of Cavan County Council. Through a public meeting and visits to schools, local people were invited to contribute objects and envelopes from anyone in their family who had emigrated. A collection point was also set up in the Post office.</p>
        <p>The installation was intended as an ephemeral piece but lasted a year because of interest, both locally and nationally.</p>
        <p>The project was greatly helped by the Cavan Arts Office and received some financial assistance from the Gulbenkian Foundation and Cavan County Council.</p>
